async function listJsFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await listJsFiles(full)));
    } else if (entry.isFile() && entry.name.endsWith(".js")) {
      files.push(full);
    }
  }
  return files;
}

function stripStars(commentBody) {
  return commentBody
    .split("\n")
    .map((line) => line.replace(/^\s*\*\s?/, "").replace(/^\s*\*$/, ""))
    .join("\n")
    .trim();
}

function parseExamples(jsdocBody) {
  const examples = [];
  const lines = jsdocBody.split("\n");
  let i = 0;

  while (i < lines.length) {
    const line = lines[i];
    const exampleMatch = line.match(/^@example(?:\s+(.*))?$/);
    if (!exampleMatch) {
      i += 1;
      continue;
    }

    let caption = "";
    const rest = (exampleMatch[1] || "").trim();
    const captionMatch = rest.match(/^<caption>([\s\S]*?)<\/caption>$/);
    if (captionMatch) {
      caption = captionMatch[1].trim();
    } else if (rest && !rest.startsWith("@")) {
      caption = rest;
    }

    i += 1;
    const bodyLines = [];
    while (i < lines.length && !/^@\w+/.test(lines[i])) {
      bodyLines.push(lines[i]);
      i += 1;
    }

    const rawBody = bodyLines.join("\n").replace(/^\n+/, "").replace(/\n+$/, "");
    if (!rawBody.trim()) continue;

    // Use "// live" (not "// @live") — TypeDoc parses "@live" as a block tag and splits examples.
    const live = /^\s*\/\/\s*live\s*$/m.test(rawBody);
    const code = rawBody
      .replace(/^\s*\/\/\s*live\s*\n?/, "")
      .replace(/\n\s*\/\/\s*live\s*$/m, "")
      .trim();

    examples.push({ caption, code, live });
  }

  return examples;
}

function parseJsdocDescription(jsdocBody) {
  const lines = jsdocBody.split("\n");
  const descLines = [];
  for (const line of lines) {
    if (/^@\w+/.test(line)) break;
    descLines.push(line);
  }
  return descLines.join("\n").trim();
}

function slugify(text, fallback) {
  const slug = String(text || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
  return slug || fallback;
}

/**
 * Collect class/function-level JSDoc immediately above each declaration.
 * Scans comment+declaration pairs so bodies cannot span earlier blocks.
 * Prefers `export` declarations when the same name appears twice.
 */
function extractAllSymbolJsdocs(source) {
  const found = new Map();
  const normalized = source.replace(/\r\n/g, "\n");
  const pattern =
    /\/\*\*([\s\S]*?)\*\/\s*(export\s+)?(?:async\s+)?(class|function|const)\s+(\w+)/g;

  for (const match of normalized.matchAll(pattern)) {
    const isExport = Boolean(match[2]);
    const name = match[4];
    const body = stripStars(match[1]);
    const parsed = {
      description: parseJsdocDescription(body),
      examples: parseExamples(body),
      isExport,
    };

    if (!parsed.description && !parsed.examples.length) continue;

    const existing = found.get(name);
    if (!existing || (isExport && !existing.isExport)) {
      found.set(name, parsed);
    }
  }

  return found;
}

async function loadAllSymbolDocs() {
  const docs = new Map();
  const files = await listJsFiles(srcDir);

  for (const file of files) {
    const source = await readFile(file, "utf8");
    const fileDocs = extractAllSymbolJsdocs(source);
    const rel = path.relative(root, file).replace(/\\/g, "/");

    for (const [name, parsed] of fileDocs) {
      const existing = docs.get(name);
      const richer =
        existing &&
        parsed.isExport &&
        existing.isExport &&
        (parsed.examples.length > existing.examples.length ||
          (!existing.description && parsed.description));
      if (!existing || (parsed.isExport && !existing.isExport) || richer) {
        docs.set(name, {
          description: parsed.description || existing?.description || "",
          examples:
            parsed.examples.length >= (existing?.examples.length || 0)
              ? parsed.examples
              : existing.examples,
          isExport: parsed.isExport || existing?.isExport || false,
          file: richer || !existing || (parsed.isExport && !existing.isExport)
            ? rel
            : existing.file,
        });
      }
    }
  }

  return docs;
}

function matchBrace(source, openIndex) {
  const open = source[openIndex];
  const close = open === "{" ? "}" : open === "(" ? ")" : "]";
  let depth = 0;
  let quote = null;
  for (let i = openIndex; i < source.length; i += 1) {
    const ch = source[i];
    const prev = source[i - 1];
    if (quote) {
      if (ch === quote && prev !== "\\") quote = null;
      continue;
    }
    if (ch === '"' || ch === "'" || ch === "`") {
      quote = ch;
      continue;
    }
    if (ch === open) depth += 1;
    else if (ch === close) {
      depth -= 1;
      if (depth === 0) return i;
    }
  }
  return -1;
}

function findExpressionEnd(source, start) {
  let depthParen = 0;
  let depthBrace = 0;
  let depthBracket = 0;
  let quote = null;
  for (let i = start; i < source.length; i += 1) {
    const ch = source[i];
    const prev = source[i - 1];
    if (quote) {
      if (ch === quote && prev !== "\\") quote = null;
      continue;
    }
    if (ch === '"' || ch === "'" || ch === "`") {
      quote = ch;
      continue;
    }
    if (ch === "(") depthParen += 1;
    else if (ch === ")") depthParen -= 1;
    else if (ch === "{") depthBrace += 1;
    else if (ch === "}") {
      if (depthBrace === 0 && depthParen === 0 && depthBracket === 0) return i;
      depthBrace -= 1;
    } else if (ch === "[") depthBracket += 1;
    else if (ch === "]") depthBracket -= 1;
    else if (
      ch === "," &&
      depthParen === 0 &&
      depthBrace === 0 &&
      depthBracket === 0
    ) {
      return i;
    }
  }
  return source.length;
}

function dedentBlock(body) {
  const lines = body.split("\n");
  const indents = lines
    .filter((line) => line.trim())
    .map((line) => line.match(/^(\s*)/)[1].length);
  const min = indents.length ? Math.min(...indents) : 0;
  return lines.map((line) => line.slice(min)).join("\n").trim();
}

/** Parse `export const NAME = { "id": () => … }` entries into demoId → code body. */
function extractDemoBuilders(source, exportName) {
  const re = new RegExp(`export\\s+const\\s+${exportName}\\s*=\\s*\\{`);
  const match = re.exec(source);
  if (!match) return new Map();

  let i = match.index + match[0].length;
  const map = new Map();

  while (i < source.length) {
    while (i < source.length && /\s/.test(source[i])) i += 1;
    if (source[i] === "}") break;
    if (source[i] === ",") {
      i += 1;
      continue;
    }
    if (source[i] !== '"') break;

    const keyEnd = source.indexOf('"', i + 1);
    const key = source.slice(i + 1, keyEnd);
    i = keyEnd + 1;
    while (i < source.length && (/\s/.test(source[i]) || source[i] === ":")) i += 1;

    const arrow = source.slice(i).match(/^(async\s*)?\(\s*\)\s*=>\s*/);
    if (!arrow) break;
    i += arrow[0].length;

    let body;
    if (source[i] === "{") {
      const end = matchBrace(source, i);
      body = dedentBlock(source.slice(i + 1, end));
      i = end + 1;
    } else {
      const end = findExpressionEnd(source, i);
      const expr = dedentBlock(source.slice(i, end).replace(/,\s*$/, ""));
      body = `return ${expr};`;
      i = end;
    }
    map.set(key, body);
  }

  return map;
}

/** Rewrite gallery namespace style (`DrawUI.X`) to named-import style for API pages. */
function rewriteGalleryCode(code) {
  return code
    .replace(/\bDrawUI\.center\b/g, "center")
    .replace(/\bDrawUI\.ICONS\b/g, "ICONS")
    .replace(/\bDrawUI\.Chart\s*\(/g, "new Chart(")
    .replace(/\bDrawUI\.Spreadsheet\s*\(/g, "new DataGrid(")
    .replace(/\bnew\s+DrawUI\./g, "new ")
    .replace(/\bDrawUI\.(\w+)/g, "$1");
}

function symbolFromApi(api) {
  if (!api) return null;
  let match = api.match(/\bnew\s+(\w+)/);
  if (match) return match[1];
  match = api.match(/\bDrawUI\.(\w+)/);
  if (match) {
    return match[1] === "Spreadsheet" ? "DataGrid" : match[1];
  }
  match = api.match(/^(\w+)\s*\(/);
  if (match) return match[1];
  match = api.match(/^(\w+)$/);
  if (match) return match[1];
  return null;
}

function primarySymbol(apis = [], knownSymbols) {
  const candidates = [];
  for (const api of apis) {
    const name = symbolFromApi(api);
    if (name) candidates.push(name);
  }
  for (const name of candidates) {
    if (knownSymbols.has(name)) return name;
  }
  return candidates[0] || null;
}

function collectManifestDemoBindings(manifest) {
  /** @type {{ demoId: string, caption: string, apis: string[] }[]} */
  const bindings = [];

  function walkItems(items) {
    for (const item of items || []) {
      if (item.demo) {
        bindings.push({
          demoId: item.demo,
          caption: item.title || item.demo,
          apis: item.apis || [],
        });
      }
      for (const sibling of item.siblings || []) {
        if (!sibling.demo) continue;
        bindings.push({
          demoId: sibling.demo,
          caption: sibling.name || item.title || sibling.demo,
          apis: [sibling.api, ...(item.apis || [])].filter(Boolean),
        });
      }
    }
  }

  for (const category of manifest.categories || []) {
    walkItems(category.items);
  }
  for (const category of manifest.peerCategories || []) {
    walkItems(category.items);
  }

  return bindings;
}

function buildGalleryLiveCatalog(manifest, demoBodies, knownSymbols) {
  /** @type {Record<string, { caption: string, code: string, demoId: string }[]>} */
  const catalog = {};
  const missing = [];

  for (const binding of collectManifestDemoBindings(manifest)) {
    const body = demoBodies.get(binding.demoId);
    if (!body) {
      missing.push(binding.demoId);
      continue;
    }
    const symbol = primarySymbol(binding.apis, knownSymbols);
    if (!symbol) continue;
    if (!catalog[symbol]) catalog[symbol] = [];
    catalog[symbol].push({
      caption: binding.caption,
      code: formatExampleCode(rewriteGalleryCode(body)),
      demoId: binding.demoId,
    });
  }

  if (missing.length) {
    console.warn(
      `Warning: manifest demos missing from demos.js / demos.full.js: ${missing.join(", ")}`,
    );
  }

  return catalog;
}

function collectLiveImports(examples, knownSymbols) {
  const used = new Set();
  for (const example of examples) {
    if (!example.live || example.demoId) continue;
    for (const name of knownSymbols) {
      const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
      const re = new RegExp(`(?:\\bnew\\s+${escaped}\\b|\\b${escaped}\\s*[.(])`);
      if (re.test(example.code)) used.add(name);
    }
  }
  return [...used].sort();
}

function formatExampleCode(body) {
  const lines = body.split("\n");
  if (lines.length < 2) return body;
  const rest = lines.slice(1);
  const indents = rest
    .filter((line) => line.trim())
    .map((line) => line.match(/^(\s*)/)[1].length);
  if (!indents.length) return body;
  const min = Math.min(...indents);
  return [
    lines[0],
    ...rest.map((line) => {
      if (!line.trim()) return "";
      return `  ${line.slice(min)}`;
    }),
  ].join("\n");
}

function renderLiveDemosModule(liveEntries, knownSymbols) {
  const fromGallery = liveEntries.filter((entry) => entry.demoId);
  const fromCode = liveEntries.filter((entry) => !entry.demoId);
  const imports = collectLiveImports(
    fromCode.map((entry) => entry.example),
    knownSymbols,
  );

  let source =
    "/* Generated by scripts/generate-docs.mjs from docs/examples/components/demos*.js (+ JSDoc // live fallbacks). Do not edit. */\n";

  if (fromGallery.length) {
    source += `import { DEMO_BUILDERS } from "../examples/components/demos.js";\n`;
    source += `import { FULL_DEMO_BUILDERS } from "../examples/components/demos.full.js";\n`;
  }

  if (imports.length) {
    source += `import {\n  ${imports.join(",\n  ")},\n} from "drawui";\n`;
  }

  source += `\n`;

  if (fromGallery.length) {
    source += `const BUILDERS = { ...DEMO_BUILDERS, ...FULL_DEMO_BUILDERS };\n\n`;
  }

  source += `export const LIVE_DEMOS = {\n`;

  for (const { id, demoId, example } of liveEntries) {
    if (demoId) {
      source += `  ${JSON.stringify(id)}: () => BUILDERS[${JSON.stringify(demoId)}](),\n`;
      continue;
    }
    const indented = example.code
      .split("\n")
      .map((line) => (line ? `    ${line}` : ""))
      .join("\n");
    source += `  ${JSON.stringify(id)}: () => {\n${indented}\n  },\n`;
  }

  source += `};\n`;
  return source;
}

function assignExampleIds(symbolName, examples) {
  const used = new Set();
  return examples.map((example, index) => {
    let base = slugify(example.caption, example.demoId || String(index));
    let id = `${symbolName}/${base}`;
    let n = 2;
    while (used.has(id)) {
      id = `${symbolName}/${base}-${n}`;
      n += 1;
    }
    used.add(id);
    return { ...example, id };
  });
}

/**
 * Gallery demos are the source of truth when present.
 * Otherwise fall back to JSDoc `@example` blocks (including `// live`).
 */
function mergeLiveCatalog(symbolName, jsdocExamples = [], galleryCatalog = {}) {
  const gallery = (galleryCatalog[symbolName] || []).map((entry) => ({
    caption: entry.caption || "",
    code: entry.code,
    live: true,
    demoId: entry.demoId,
  }));

  if (gallery.length) {
    const nonLive = jsdocExamples.filter((example) => !example.live);
    return [...nonLive, ...gallery];
  }

  return jsdocExamples;
}

function renderSymbolPage(symbol, docs) {
  const importPath = primaryImport(symbol.entrypoints);
  const importStatement =
    importPath === "drawui"
      ? `import { ${symbol.name} } from "drawui";`
      : `import { ${symbol.name} } from "${importPath}";`;

  const description = docs?.description || symbol.notes;
  const examples = docs ? assignExampleIds(symbol.name, docs.examples) : [];

  let markdown = `${GENERATED_BANNER}# ${symbol.name}

**Status:** ${STATUS_LABEL[symbol.status]}  
**Category:** ${symbol.category}  
**Import:** ${formatEntrypoints(symbol.entrypoints)}  
**Emitted class(es):** ${formatEmits(symbol.emits)}

${description}
`;

  if (docs?.file) {
    markdown += `\n_Source: \`${docs.file}\`_\n`;
  }

  markdown += `
## Import

\`\`\`js
${importStatement}
\`\`\`
`;

  if (examples.length) {
    markdown += `\n## Examples\n`;
    for (const example of examples) {
      const heading = example.caption || "Usage";
      markdown += `\n### ${heading}\n\n`;
      markdown += `\`\`\`js\n${example.code}\n\`\`\`\n`;
      if (example.live) {
        markdown += `\n<div class="doc-live-demo" data-live-demo="${example.id}" aria-label="Live preview"></div>\n`;
      }
    }
  }

  markdown += `\n[← Back to API index](./index.html)\n`;
  return { markdown, examples };
}


function rewriteMarkdownLinks(markdown) {
  return markdown.replace(/\]\(([^)]+\.md)(#[^)]*)?\)/g, (_match, file, hash = "") => {
    return `](${file.replace(/\.md$/, ".html")}${hash})`;
  });
}

function markdownToHtml(markdown) {
  const body = markdown.replace(/^<!--[\s\S]*?-->\n\n?/, "");
  return markdownConverter.makeHtml(rewriteMarkdownLinks(body));
}


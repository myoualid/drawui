function extractNamedExports(source) {
  const names = new Set();

  for (const match of source.matchAll(/^export\s+(?:async\s+)?(?:class|function)\s+(\w+)/gm)) {
    names.add(match[1]);
  }

  for (const match of source.matchAll(/^export\s+\{\s*([^}]+)\s*\}/gm)) {
    for (const part of match[1].split(",")) {
      const trimmed = part.trim();
      if (!trimmed) continue;
      const alias = trimmed.split(/\s+as\s+/).pop().trim();
      names.add(alias);
    }
  }

  for (const match of source.matchAll(/^export\s+\*\s+from\s+["']([^"']+)["']/gm)) {
    names.add(`*:${match[1]}`);
  }

  return names;
}

async function loadExportSurface(entrypoint, visited = new Set()) {
  const relativePath = ENTRYPOINT_FILES[entrypoint];
  if (!relativePath) {
    throw new Error(`Unknown entrypoint mapping: ${entrypoint}`);
  }

  const absolutePath = path.join(root, relativePath);
  if (visited.has(absolutePath)) {
    return new Set();
  }
  visited.add(absolutePath);

  const source = await readFile(absolutePath, "utf8");
  const names = extractNamedExports(source);

  for (const marker of [...names].filter((name) => name.startsWith("*:"))) {
    names.delete(marker);
    const importPath = marker.slice(2);
    const resolved = path.normalize(
      path.join(path.dirname(absolutePath), importPath.endsWith(".js") ? importPath : `${importPath}.js`),
    );
    const reExported = await loadExportSurfaceFromFile(resolved, visited);
    reExported.forEach((name) => names.add(name));
  }

  if (entrypoint === "drawui") {
    names.add("DrawUI");
    for (const barrel of ["src/exports/shared.js", "src/components/peer/index.js"]) {
      const barrelNames = await loadExportSurfaceFromFile(
        path.join(root, barrel),
        new Set([...visited]),
      );
      barrelNames.forEach((name) => names.add(name));
    }
  }

  return names;
}

async function loadExportSurfaceFromFile(absolutePath, visited) {
  if (visited.has(absolutePath)) {
    return new Set();
  }
  visited.add(absolutePath);

  const source = await readFile(absolutePath, "utf8");
  const names = extractNamedExports(source);

  for (const marker of [...names].filter((name) => name.startsWith("*:"))) {
    names.delete(marker);
    const importPath = marker.slice(2);
    const resolved = path.normalize(
      path.join(path.dirname(absolutePath), importPath.endsWith(".js") ? importPath : `${importPath}.js`),
    );
    const nested = await loadExportSurfaceFromFile(resolved, visited);
    nested.forEach((name) => names.add(name));
  }

  return names;
}

function validateRegistry(registry, exportSurfaces, packageJson) {
  const errors = [];
  const seen = new Set();

  for (const symbol of registry.symbols) {
    if (seen.has(symbol.name)) {
      errors.push(`Duplicate registry symbol: ${symbol.name}`);
      continue;
    }
    seen.add(symbol.name);

    let found = false;
    for (const entrypoint of symbol.entrypoints) {
      const exports = exportSurfaces.get(entrypoint);
      if (!exports) {
        errors.push(`${symbol.name}: unknown entrypoint ${entrypoint}`);
        continue;
      }
      if (exports.has(symbol.name)) {
        found = true;
      }
    }

    if (!found) {
      errors.push(`${symbol.name} is not exported from any listed entrypoint`);
    }
  }

  const publishedCss = Object.keys(packageJson.exports).filter((key) => key.endsWith(".css"));
  for (const css of registry.cssEntryPoints) {
    if (css.status === "internal") continue;
    const exportKey = `./${css.path}`;
    if (!publishedCss.includes(exportKey)) {
      errors.push(`CSS entry ${css.path} is marked ${css.status} but missing from package.json exports`);
    }
  }

  return errors;
}

function collectManifestApis(manifest) {
  const apis = new Set();

  function walkItems(items = []) {
    for (const item of items) {
      for (const api of item.apis ?? []) {
        apis.add(api);
      }
      for (const bucket of [item.siblings, item.alternatives, item.parts, item.variants]) {
        walkItems(bucket);
      }
    }
  }

  for (const category of manifest.categories ?? []) {
    walkItems(category.items);
  }

  for (const category of manifest.peerCategories ?? []) {
    walkItems(category.items);
  }

  if (manifest.peerCategory?.items) {
    walkItems(manifest.peerCategory.items);
  }

  return apis;
}

function manifestApiSymbol(api) {
  if (/^DrawUI\.\w+/.test(api)) {
    return null;
  }

  const ctor = api.match(/^new\s+(\w+)\s*\(/);
  if (ctor) {
    return ctor[1];
  }

  return api.replace(/\(\.\.\.\)$/, "").replace(/\([^)]*\)$/, "").trim().split(/[\s.(]/)[0];
}

function validateManifestApis(manifest, registry) {
  const warnings = [];
  const symbolNames = new Set(registry.symbols.map((symbol) => symbol.name));

  for (const api of collectManifestApis(manifest)) {
    const symbol = manifestApiSymbol(api);
    if (!symbol) {
      continue;
    }

    if (!symbolNames.has(symbol)) {
      warnings.push(`Manifest API "${api}" does not match a registry symbol`);
    }
  }

  return { warnings };
}

